CREATE TRIGGER `after_items_update` AFTER UPDATE ON `items` FOR EACH ROW BEGIN
    IF OLD.stock_level <> NEW.stock_level THEN
        INSERT INTO inventory_logs (
            item_id,
            old_quantity,
            new_quantity,
            quantity_added,
            user_name
        )
        VALUES (
            NEW.item_id,
            OLD.stock_level,
            NEW.stock_level,
            (NEW.stock_level - OLD.stock_level),
            'SYSTEM_AUTO'
        );
    END IF;
END
$$
DELIMITER ;
DELIMITER $$
CREATE TRIGGER `trg_log_manual_restocks` AFTER UPDATE ON `items` FOR EACH ROW BEGIN
    -- ONLY log if the quantity INCREASED (New > Old)
    -- This ignores sales (which decrease the number)
    IF NEW.stock_level > OLD.stock_level THEN
        INSERT INTO inventory_logs (
            item_id, 
            quantity_added, 
            old_quantity, 
            new_quantity, 
            change_type
        )
        VALUES (
            NEW.item_id, 
            (NEW.stock_level - OLD.stock_level), 
            OLD.stock_level, 
            NEW.stock_level, 
            'RESTOCK'
        );
    END IF;
END
$$
DELIMITER ;
DELIMITER $$
CREATE TRIGGER `trg_manage_low_stock` AFTER UPDATE ON `items` FOR EACH ROW BEGIN
    -- 1. LOG NEW ALERT: If stock drops into the 'low' zone
    IF NEW.stock_level <= 5 AND OLD.stock_level > 5 THEN
        INSERT INTO low_stock_alerts (item_id, stock_level, resolved)
        VALUES (NEW.item_id, NEW.stock_level, 0);
    END IF;

    -- 2. AUTO-RESOLVE: If stock is refilled above the threshold
    IF NEW.stock_level > 5 AND OLD.stock_level <= 5 THEN
        UPDATE low_stock_alerts 
        SET resolved = 1 
        WHERE item_id = NEW.item_id AND resolved = 0;
    END IF;
END
$$
DELIMITER ;

